package ClassObjectConcept;

public class FinalVarConcept {

	public static void main(String[] args) {

		final int days = 7; // number of days in a week is fixed so we can defined it as final so no one can
							// change it
		int salary = 100 * days;
		System.out.println(salary);

		final short months = 12; // 12 number of months in a year
		// Logo, Header and footer are fixed then we can defined constant values.

	}

}
